// A simple C++ program
#include <iostream> // This is a preprocessor directive which starts with # symbol
                    // iostream is a header file, and brackets <> indicate that it is a standard C++ header file.
using namespace std; // C++ uses namespaces to organize the names of program entities.
                     // The program needs access to the std namespace is because every name created by the iostream file
                     // is part of that namespace.
int main() { // Left or opening brace.
    cout << "Programming is grat fun!"; // The group of chars inside the quotation marks: string literal or constant.
    return 0;
} // Right or closing brace.

// //  Double slash
// #   Pound sign
// < > Opening and closing brackets
// ( ) Opening and closing parentheses
// { } Opening and closing braces
// " " Opening and closing quotation marks
// ;   Semicolon
