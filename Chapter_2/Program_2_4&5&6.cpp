// An unruly printing program
#include <iostream>
using namespace std;

int main() {
    // A well-adjusted printing program with stream manipulator endl
    cout << "The following items were top sellers" << endl;
    cout << "during the month of June" << endl;
    cout << "Computer games" << endl;
    cout << "Coffee" << endl;
    cout << "Aspirin" << endl;

    // Yet another well-adjusted printing program with newline escape sequence
    cout << "The following items were top sellers\n";
    cout << "during the month of June\n";
    cout << "Computer games\nCoffee";
    cout << "\nAspirin\n";
    return 0;
}

// \n  Newline
// \t  Horizontal tab
// \a  Alarm
// \b  Backspace
// \r  Return
// \\  Backslash
// \' Single quote
// \" Double quote

